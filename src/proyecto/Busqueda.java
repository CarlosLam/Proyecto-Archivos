    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        // TODO add your handling code here:
        try{
            
            Boolean encontrado = false;
            String linea = "";
            File v = new File("C:\\MEIA\\Master.txt");
            FileReader frpp = new FileReader(v);
            BufferedReader brpp = new BufferedReader(frpp);
            File w = new File("C:\\MEIA\\usuario.txt");
            FileReader fr = new FileReader(w);
            BufferedReader br = new BufferedReader(fr);
            
            while((linea=br.readLine())!= null){
                String usuario = linea.substring(0,linea.indexOf("|"));
                if(usuario.equals(Busq.getText())){
                    JOptionPane.showMessageDialog(rootPane, "Usuario encontrado");
                    encontrado = true;
                    break;
                }
            }
            if(!encontrado){
                while((linea=brpp.readLine()) != null){
                    String usuario = linea.substring(0,linea.indexOf("|"));
                    if(usuario.equals(Busq.getText())){
                        JOptionPane.showMessageDialog(rootPane, "Usuario encontrado");
                        encontrado = true;
                        break;
                    }
                }
            }
            if(encontrado == false){
                 JOptionPane.showMessageDialog(rootPane, "Usuario no encontrado");
            }
      
        }catch(FileNotFoundException e){
            
        } catch (IOException ex) {
            Logger.getLogger(Busqueda.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_BuscarActionPerformed

